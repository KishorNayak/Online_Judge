#include <iostream>
using namespace std;
int main() {
    // Declare variables
    float num1, num2, result;
    // Take input from user
    cout << "Enter first number: ";
    cin >> num1;
    cout << "Enter second number: ";
    cin >> num2;
    // Multiply the numbers
    result = num1 * num2;
    // Display the result
    cout << "Multiplication result: " << result << endl;
    return 0;
}